      // Dynamically load the top-message.html and leadsform.html
      // into the #alternate-content container.
      function loadAlternateContent() {
        const container = document.getElementById("alternate-content");

        // URL paths where you placed these files on your server
        const topMessageUrl = "top-message.html";
        const leadsFormUrl = "leadsform.html";

        // 1) Load top-message.html
        fetch(topMessageUrl)
