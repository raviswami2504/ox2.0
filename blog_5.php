                        <article>

                          <p>Brilliant! With smart IoT integration, Oxmaint cement optimization suite is the
                            most efficient technology to digitally-connected products, services and solutions.
                            Integrated digital solutions to improve yield, quality, production costs and energy
                            efficiency. With mobile app driven transformation journey that gets real-time information
                            visibility across the plant and HO. The result: improved operational efficiencies of
                            plants, net 10% reduction in maintenance cost, with a 10% reduction in annual energy
                            consumption plus a 12% reduction in heat consumption for each kilogram of raw material
                            produced. It’s about time for your factory!</p>


                          <p><b> Oxmaint App</b> : An end-to-end digital system to optimize all production processes
                            in the cement plant. Real-time visibility vis-à-vis optimization of all areas of the
                            plant. The iFactory app is the integrated digital innovation for the cement industry that
                            brings about a new way of working at all levels of production in the plant and within the
                            company itself.</p>

                          <p> <b>Raw Material Optimization (RMO)</b> : Intelligent management, handling and monitoring
                            of raw material and fuel systems via mobile app. Monitoring and optimization of spare
                            parts and material stock levels, cost, as well as real-time reporting & analysis system.
                            Analysis of historic data to proactively predict equipment maintenance requirements, avoid
                            unnecessary downtime and loss in performance.</p>
                          <p> <b>Yield Improvement </b>: Centralized process and plant planning via mobile app,
                            enabling full visibility of production processes from the source to the end market. It
                            allows for a real-time adjustment in yields based on demand, stock levels, cost and
                            energy. It is an efficient way to adjust yield to maximize production without the worry of
                            not meeting the demands.</p>

                          <p> <b>Walk By Inspection & Maintenance</b> : Efficiently plan daily walk by inspection,
                            reminders
                            and missed report, provide multi language option to record inspection faults, workflow to
                            immediate notification, approvals via mobile app, coordination with store to get spare
                            parts. All information is managed via mobile apps to significantly improve maintenance
                            team productivity. Hardware attached to automatically capture the inspection location and
                            ensure inspection is conducted. </p>

                          <p> <b>Instrumentation & Control (I&C) </b>: Connects all I&C systems of the cement plant
                            via mobile app for significant process monitoring and optimization parameters such as
                            power consumption, yield, kiln temperatures and many more.</p>

                          <p> <b>Quality (Quality Assurance) </b>: Centralized monitoring via mobile app of all
                            quality parameters in the plant with real-time analysis and reporting of corrective
                            actions. Such as, moisture content, air and fire temperature and much more.</p>

                          <p> <b>Pulverization & Raw Materials </b>: Real-time optimization and monitoring of the raw
                            materials supply through mobile app along with pulverization technology integration to
                            further reduce costs.</p>

                          <p> <b>Cement Plant App </b>: Provides easy access to any data from the plant. All data
                            collected locally can be accessed online for easy analysis and for room for updates and
                            improved performance in the plant. A tailored app with all the systems of a containerized
                            cement plant digitized, connected and integrated into one mobile app. The information is
                            available 24/7 through the mobile app, which can be accessed anywhere anytime by an
                            authorized user, no matter where in the world they are.</p>

                          <p><b> AI (Artificial Intelligence) supervised Integrated control systems data within App
                            </b> : Software solution to enable improved energy efficiency and reduce CO2 emissions.
                            Energy optimization platform via mobile app that allows real-time energy consumption and
                            performance monitoring of all devices connected to the digital platform. With these
                            analytics, it will help optimize energy consumption, while reducing the emission of
                            pollutants in cement plants.</p>

