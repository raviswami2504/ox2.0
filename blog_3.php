                        <h2 class="h1 mb-4">Plant maintenance 10 ways to Keep assets in peak operating condition</h2>
                        <article>
                          <p>
                            "Should I automate my plant maintenance?" is a question that comes up all the time. After
                            all, time is money, right? Automation can work wonders in keeping your assets in peak
                            operations and maximizing production capacity.
                          </p>
                        </article>
                        <article>
                          <p>
                          <h3>Here are some tips on how to get there:</h3>
                          </p>

                          <ul>
                            <p> 1. Continuous Improvement:</p> Set specific goals for change at the team level and
                            implement new improvement opportunities as they arise. Employees at every level of the
                            organization should be involved in identifying possible improvements with constant feedback
                            to management throughout their implementation so that everyone feels ownership of the
                            process.
                          </ul>

                          <ul>
                            <p> 2. Understand what you don't know:</p> Ninety percent of all maintenance issues are
                            caused by an unexpected problem, and the root cause of 80% of these unknown problems is
                            employee ignorance about what they need to do.
                          </ul>

                          <ul>
                            <p> 3. Measure for improvement:</p> Regularly measure output and process efficiency in order
                            to determine where your bottlenecks are, how to improve the organization, and which parts of
                            the operation need further investment.

                          </ul>


                          <ul>
                            <p> 4. Live with no surprises: </p>The biggest problems with maintenance overhead come from
                            leaving something undone, from making a mistake at production when one minute before the
                            machine was running smoothly, or from not knowing what resources (people or materials)
                            you'll need for a fix before it happens. Therefore, make maintenance a priority and plan
                            ahead.
                          </ul>


                          <ul>
                            <p> 5. Think for yourself: </p>The most important thing you can do to improve your
                            maintenance capability is to think for yourself and avoid falling back into the trap of
                            using someone else's pre-packaged solution. If it isn't in your best interest or supports
                            what you want, don't accept it! There are too many "solutions" out there that aren't as good
                            as the one you could develop if you took the time to understand what your plant needs then
                            implement.
                          </ul>


                          <ul>
                            <p>6. Save money now and pay later: </p>The cost of downtime is not measured in dollars but
                            in the loss of a realized profit opportunity (as well as lost revenue). Therefore, the only
                            time it really makes sense to automate maintenance is when there is a high risk of downtime,
                            a high probability of lost production, and budget that allows for an expense.
                          </ul>


                          <ul>
                            <p>7. Plan far enough ahead: </p>Electricity, transportation, water and other
                            "infrastructure" costs are fixed over time so that taking the "short-term" approach in
                            running your operation will cost you more in the long run than investing in your future with
                            detailed planning and design that considers what you want to do. Think about maintenance as
                            if it were planning for next year's infrastructure needs and add up all the costs of doing
                            it manually or automatically. Then put these numbers in relation to how much they're costing
                            you per day in lost production today.
                          </ul>


                          <ul>
                            <p>8. Use technology with caution: </p>An automated system is only as good as the people
                            that build it, but that doesn't mean technology will solve all maintenance issues by itself.
                            The most important thing to remember is that maintaining your plant isn't a technological
                            task. It's a human one and in the end requires technical experts.
                          </ul>


                          <ul>
                            <p> 9. Think of your maintenance strategy holistically and use it wisely: </p>Just because
                            you have a reliable maintenance solution doesn't mean it's ready to be used on its own
                            without training, or without careful consideration of other systems in place, or without
                            considering how other systems may affect your operations. Start by looking for other
                            efficiencies and improvements before investing in your maintenance solution.
                          </ul>


                          <ul>
                            <p>10. Choose the right maintenance solution: </p>Beyond just the technology, choose the
                            right people to implement it and give them the training needed to run it smoothly and
                            efficiently.
                          </ul>


